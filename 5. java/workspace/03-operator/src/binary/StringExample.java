package binary;

public class StringExample {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
	
		// String 문자참조형, 여러글자 쓸수있음, "" 반드시 쌍따옴표
		// 문자열 + 숫자 = 문자열
		String str1 = "JDK " + 6.0;
		System.out.println(str1);
		
		String msg = "Hello," + " Java";
		System.out.println(msg);
	}

}
